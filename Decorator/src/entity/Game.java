package entity;

public abstract class Game {

   protected String tenGame = "NULL";
   protected String theLoai  = "NULL";
   protected String phuKien  = "";

    public String getTenGame() {
        return tenGame;
    }

    public String getTheLoai() {
        return theLoai;
    }

    public String getPhuKien() {
        return phuKien;
    }
}
